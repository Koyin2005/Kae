#[derive(Clone, Copy,Debug)]
pub struct Token<'a>{
    pub  lexeme : &'a str,
    pub  kind : TokenKind,
    pub line : u32,
}
#[derive(PartialEq, Eq,Clone, Copy,Debug)]
pub enum TokenKind{
    Semicolon,
    Star,
    Slash,
    Plus,
    Minus,
    Equals,
    Bang,
    Lesser,
    Greater,
    LeftBrace,
    RightBrace,
    LeftParen,
    RightParen,
    LeftBracket,
    RightBracket,
    Dot,
    Coma,
    Colon,
    QuestionMark,

    EqualsEquals,
    BangEquals,
    LesserEquals,
    GreaterEquals,
    FatArrow,
    ThinArrow,

    DoubleColon,
    Dots,

    Int,
    Float,
    String,

    Trait,
    Ref,
    Match,
    If, 
    While,
    And,
    Or,
    Else,
    Fun,
    Struct,
    Enum,
    True,
    False,
    Let,
    Is,
    Print,
    Impl,
    LowerSelf,
    UpperSelf,
    Return,
    Typename,
    
    Identifier,
    Wildcard,
    Eof
}
